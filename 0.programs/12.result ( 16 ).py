

# w.a.p to read a character and print appropriate message as follows.
# A.Excellent,, B.Good,, C. Fair,, D.Poor,, F. Fail.

ch = input('Enter a Character: ')

if ch == 'a':
    print('Excellent')
elif ch == 'b':
    print('Good')
elif ch == 'c':
    print('Fair')
elif ch == 'd':
    print('Poor')
elif ch == 'f':
    print('Fail')
else:
    print('Invalid input')
